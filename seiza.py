def get_zodiac_sign(month, day):
    for sign, (start_month, start_day), (end_month, end_day) in zodiac_signs:
        if (month == start_month and day >= start_day) or (month == end_month and day <= end_day):
            return sign
        # 山羊座のように年を跨ぐ場合
        if sign == "山羊座" and ((month == 12 and day >= 22) or (month == 1 and day <= 19)):
            return sign
        if sign == "水瓶座" and ((month == 1 and day >= 20) or (month == 2 and day <= 18)):
            return sign
        if sign == "魚座" and ((month == 2 and day >= 19) or (month == 3 and day <= 20)):
            return sign
    return None


def make_daily_seed(birthdate):
    today = datetime.today().date()
    key = f"{birthdate.isoformat()}_{today.isoformat()}"
    seed = 0
    for ch in key:
        seed = (seed * 1315423911 + ord(ch)) & 0xFFFFFFFFFFFFFFFF
    return seed


lucky_colors = [
    "赤", "青", "黄色", "緑", "紫", "ピンク", "オレンジ", "黒", "白", "ゴールド", "シルバー", "水色"
]

lucky_items = [
    "財布", "香水", "ハンカチ", "ペン", "スマホケース", "ネックレス", "スニーカー", "傘", "手帳", "アクセサリー", "カップ", "ノート"
]


def get_fortune(sign, birthdate):
    seed = make_daily_seed(birthdate)
    rnd = random.Random(seed)
    fortune = {}
    for category in ["恋愛運", "仕事運", "健康運"]:
        fortune[category] = rnd.choice(zodiac_fortunes[sign][category])
    color = rnd.choice(lucky_colors)
    item = rnd.choice(lucky_items)
    fortune["ラッキーアイテム"] = f"{color}の{item}"
    return fortune


def main():
    print("ようこそ、12星座占いへ。")
    while True:
        birthdate_str = input("生年月日を YYYY-MM-DD の形式で入力してください: ").strip()
        try:
            birthdate = datetime.strptime(birthdate_str, "%Y-%m-%d")
            month = birthdate.month
            day = birthdate.day
            sign = get_zodiac_sign(month, day)
            if sign:
                break
            else:
                print("無効な日付です。もう一度入力してください。")
        except ValueError:
            print("入力形式が正しくありません。YYYY-MM-DD の形式で入力してください。")

    print(f"\nあなたの星座は {sign} です。")
    print("今日の運勢を占います...")

    fortune = get_fortune(sign, birthdate)
    print("\n🔮 今日のあなたの運勢 🔮")
    for category, message in fortune.items():
        print(f"\n【{category}】")
        print(message)

    print("\n✨ 運勢は日々変化します。ポジティブな気持ちを大切にしましょう。")


