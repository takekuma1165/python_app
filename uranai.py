# ===== 基本関数 =====
def reduce_number(num, keep_master=True):
    while True:
        if keep_master and num in [11, 22, 33]:
            return num
        if num < 10:
            return num
        num = sum(int(d) for d in str(num))

def sum_digits(s):
    return sum(int(c) for c in s if c.isdigit())

# ===== 数秘計算 =====
def life_path_number(birthdate):
    return reduce_number(sum_digits(birthdate))

def personal_year(birthdate, today):
    return reduce_number(sum_digits(birthdate[5:]) + sum_digits(today.strftime("%Y")))

def personal_month(py, today):
    return reduce_number(py + today.month)

def personal_day(pm, today):
    return reduce_number(pm + today.day)

